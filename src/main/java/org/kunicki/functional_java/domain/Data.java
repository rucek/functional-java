package org.kunicki.functional_java.domain;

class Data {
    private Long id;
    private String value;

    public Data(Long id, String value) {
        this.id = id;
        this.value = value;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    // equals, hashCode, toString
}
